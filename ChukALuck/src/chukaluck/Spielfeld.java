    private boolean maxDrei(){
        int counter = 0;
        if (cb_Nr1.isSelected()) {
            counter++;
        }
        if (cb_Nr2.isSelected()) {
            counter++;
        }
        if (cb_Nr3.isSelected()) {
            counter++;
        }
        if (cb_Nr4.isSelected()) {
            counter++;
        }
        if (cb_Nr5.isSelected()) {
            counter++;
        }
        if (cb_Nr6.isSelected()) {
            counter++;
        }
        
        if (counter < 3) 
            return true;
        else
            return false;
    }
    
